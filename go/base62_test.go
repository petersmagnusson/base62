package base62

import (
	"testing"
)

type testCase struct {
	buffer []byte
	base62 string
}

const DBG = true

func TestArrayBufferToBase62(t *testing.T) {
    for _, tc := range testCases {
        encoded := ArrayBufferToBase62(tc.buffer)
        if encoded != tc.base62 {
				if DBG {
					t.Logf("ERROR: Testing ArrayBufferToBase62 with input %v, expected base62 %s, got %s", tc.buffer, tc.base62, encoded)
				}
				t.Errorf("ArrayBufferToBase62(%v) = %s; want %s", tc.buffer, encoded, tc.base62)
        }
    }
}

func TestBase62ToArrayBuffer(t *testing.T) {
    for _, tc := range testCases {
        decoded, err := Base62ToArrayBuffer(tc.base62)
        if err != nil {
            t.Logf("ERROR: Testing Base62ToArrayBuffer with base62 '%s', expected buffer %v, got %v", tc.base62, tc.buffer, decoded)
            t.Errorf("Base62ToArrayBuffer(%s) returned error: %v", tc.base62, err)
        }
        if !equal(decoded, tc.buffer) {
            t.Errorf("Base62ToArrayBuffer(%s) = %v; want %v", tc.base62, decoded, tc.buffer)
        }
    }
}


func TestIllegalBase62Strings(t *testing.T) {
	for _, s := range illegalBase62strings {
		_, err := Base62ToArrayBuffer(s)
		if err == nil {
			t.Errorf("Expected error for Base62ToArrayBuffer(%s), but got none", s)
		}
	}
}

func equal(a, b []byte) bool {
    // Check if both slices are nil or empty, which are considered equal
    if len(a) == 0 && len(b) == 0 {
        return true
    }
    if len(a) != len(b) {
        return false
    }
    for i, v := range a {
        if v != b[i] {
            return false
        }
    }
    return true
}

